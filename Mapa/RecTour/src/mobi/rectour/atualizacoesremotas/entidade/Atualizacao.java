package mobi.rectour.atualizacoesremotas.entidade;

public class Atualizacao {
	
	private String nome_tabela  ;
	private String dataUltimaAtualizacao; 	
	private String determinarAtualizacao;
	private String descricaoLogUsuario;
	private String descricaoLogDev;
	
	public Atualizacao(){
		nome_tabela = "";
		dataUltimaAtualizacao = "0";
		determinarAtualizacao = "S";
		descricaoLogUsuario = "";
		descricaoLogDev     = "";
	}
	
	
	public String getNome_tabela() {
		return nome_tabela;
	}
	public void setNome_tabela(String nome_tabela) {
		this.nome_tabela = nome_tabela;
	}
	public String getDataUltimaAtualizacao() {
		return dataUltimaAtualizacao;
	}
	public void setDataUltimaAtualizacao(String dataUltimaAtualizacao) {
		this.dataUltimaAtualizacao = dataUltimaAtualizacao;
	}
	public String getDeterminarAtualizacao() {
		return determinarAtualizacao;
	}
	public void setDeterminarAtualizacao(String determinarAtualizacao) {
		this.determinarAtualizacao = determinarAtualizacao;
	}


	public String getDescricaoLogUsuario() {
		return descricaoLogUsuario;
	}


	public void setDescricaoLogUsuario(String descricaoLogUsuario) {
		this.descricaoLogUsuario = descricaoLogUsuario;
	}


	public String getDescricaoLogDev() {
		return descricaoLogDev;
	}


	public void setDescricaoLogDev(String descricaoLogDev) {
		this.descricaoLogDev = descricaoLogDev;
	}


	
	

}
